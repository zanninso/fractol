#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*r;

	while (*str)
	{
		if (*str == c)
			return ((r = ft_strrchr(str + 1, c)) ? r : (char *)str);
		str++;
	}
	return (*str || c == '\0' ? (char *)str : 0);
}
